# Read the existing schematic
sch_path = os.path.join(OUT, 'esp32-epaper.kicad_sch')
with open(sch_path, 'r') as f:
    content = f.read()

# Find the lib_symbols closing bracket
# Look for "  )\n\n" after "(lib_symbols"
lib_idx = content.index('(lib_symbols')
close_idx = content.index('\n  )\n', lib_idx)

# Insert standard symbols before the closing bracket
insert = '\n'.join([DEVICE_C, DEVICE_R, DEVICE_D, DEVICE_L, DEVICE_LED, DEVICE_Q, ESP32])
content = content[:close_idx] + '\n' + insert + content[close_idx:]

# Fix ESP32 pin count: 39 -> 30 (matching our 30-pin lib symbol)
# Count pin UUIDs for ESP32 instance
esp32_idx = content.index('(lib_id "RF_Module:ESP32-WROOM-32D")')
esp32_end = content.index('\n  )\n', esp32_idx)

# Replace 39 pin UUIDs with 30
import re
esp32_block = content[esp32_idx:esp32_end]
pin_lines = re.findall(r'    \(pin "\d+" \(uuid [^)]+\)\)', esp32_block)
print(f"ESP32 has {len(pin_lines)} pin UUIDs (need 30)")

if len(pin_lines) > 30:
    # Remove extra pins (31-39)
    for p in pin_lines[30:]:
        esp32_block = esp32_block.replace(p, '', 1)
    content = content[:esp32_idx] + esp32_block + content[esp32_end:]
    print(f"Trimmed to 30 pins")

# Write back
with open(sch_path, 'w') as f:
    f.write(content)

# Verify: check all lib_ids now have matching definitions
lines = content.split('\n')
lib_ids = set()
inst_ids = set()
for line in lines:
    if '(symbol "' in line and 'lib_symbols' not in line:
        s = line.index('"') + 1
        e = line.index('"', s)
        lib_ids.add(line[s:e])
    if '(lib_id "' in line:
        s = line.index('"') + 1
        e = line.index('"', s)
        inst_ids.add(line[s:e])

missing = inst_ids - lib_ids
print(f"\nLib symbols: {len(lib_ids)}, Instances: {len(inst_ids)}")
if missing:
    print(f"STILL MISSING: {sorted(missing)}")
else:
    print("✓ All instance lib_ids have matching lib_symbols definitions!")

print(f"\nFile size: {len(content)} bytes")
